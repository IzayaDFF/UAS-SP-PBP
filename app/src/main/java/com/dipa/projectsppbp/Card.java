package com.dipa.projectsppbp;

public class Card {

    int image;
    String judul;

    public Card(int image, String judul) {
        this.image = image;
        this.judul = judul;
    }

    public int getImage() {
        return image;
    }

    public String getJudul() {
        return judul;
    }

}
